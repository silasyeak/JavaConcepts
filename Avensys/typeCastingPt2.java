package com.java.programs;

public class typeCastingPt2 {

	public static void main(String[] args) {
		byte a = 10;
		short b = 20;
		int c = 30;
		long d = 40;
		float e = 5.00f;
		double f = 60.0;
		char ch = 'A';
		boolean bo = true;
		
		//new is used to create an object
		//wrapper classes
		Byte a = new Byte(10);
		Short b = new Short(20);
		Integer c = new Integer(30);
		Long d = new Long(50);
		Float e = new Float(3.14);
		Double f = new Double(50.0);
		Character ch = new Character('A');
		Boolean bo = new Boolean(true);

	}

}
