package com.java.programs;

public class byteShortDemo {

	public static void main(String[] args) {
		byte a = 45;
		short b;
		b = a;
		System.out.println(b);
		
		int i = 10;
		long j;
		j = i;
		System.out.println(j);
		
		//use f as the suffix for float
		float x = 200.0f;
		double y = (double)x;
		System.out.println(y);
		
		//char to int
		char g = 'a';
		int h = g;
		System.out.println(h);
		
		
	

	}

}
